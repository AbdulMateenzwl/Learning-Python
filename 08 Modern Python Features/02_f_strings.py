name = "Mateen"
score = 95

print(f"Hello, {name}! Your score is {score}.")
# Output: Hello, Mateen! Your score is 95.

print(f"Next year, your age will be {20 + 1}")
# Output: Next year, your age will be 21

pi = 3.14159
print(f"Pi is approximately {pi:.2f}")  # Output: Pi is approximately 3.14
print(f"Binary of 10 is {10:b}")  # Output: Binary of 10 is 1010

