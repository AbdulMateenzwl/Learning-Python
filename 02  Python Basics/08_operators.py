# Arithmetic Operators
a = 10
b = 3

print("Addition:", a + b)        # 13
print("Subtraction:", a - b)     # 7
print("Multiplication:", a * b)  # 30
print("Division:", a / b)        # 3.333...
print("Floor Division:", a // b) # 3
print("Modulus:", a % b)         # 1
print("Power:", a ** b)          # 1000

# Comparison Operators
print("Equal:", a == b)      # False
print("Not equal:", a != b)  # True
print("Greater:", a > b)     # True
print("Less:", a < b)        # False

# Logical Operators
x = True
y = False

print("AND:", x and y)   # False
print("OR:", x or y)     # True
print("NOT:", not x)     # False

# Bitwise Operators (work on bits)
print("Bitwise AND:", a & b)  # 10 & 3 = 2
print("Bitwise OR:", a | b)   # 10 | 3 = 11
print("Bitwise XOR:", a ^ b)  # 10 ^ 3 = 9
print("Left Shift:", a << 1)  # 10 << 1 = 20
print("Right Shift:", a >> 1) # 10 >> 1 = 5

# Assignment Operators
c = 5
c += 2  # same as c = c + 2
print("c after += 2:", c)
